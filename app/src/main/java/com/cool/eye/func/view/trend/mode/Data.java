package com.cool.eye.func.view.trend.mode;

public class Data {

    private float x;
    private float y;
    private long valueX;
    private float valueY;

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    public long getValueX() {
        return valueX;
    }

    public void setValueX(long valueX) {
        this.valueX = valueX;
    }

    public float getValueY() {
        return valueY;
    }

    public void setValueY(float valueY) {
        this.valueY = valueY;
    }

}
